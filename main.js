function createNewCardContainer(page, cardInfo, card) {
    // Create Outer Container
    const cardContainer = document.createElement('div')
    cardContainer.classList.add('card-outer-container')

    // Create the Title inside the Outer Container
    const titleContainer = document.createElement('div')
    titleContainer.classList.add('title-container')
    titleContainer.innerHTML = `
        <h3>${cardInfo.title}</h3>
    `
    cardContainer.appendChild(titleContainer)

    // Create the Inner Container
    const cardInnerContainer = document.createElement('div')
    cardInnerContainer.classList.add('card-inner-container')

    // Create Sections inside the Inner Container
    for (const section in cardInfo.sections) {

        // New Section Element
        const newSection = document.createElement('section')
        newSection.classList.add('group-section-container')

        // Create Section Title
        const newSectionTitle = document.createElement('h3')
        newSectionTitle.classList.add('group-title')
        newSectionTitle.textContent = `${cardInfo.sections[section].title}`
        newSection.appendChild(newSectionTitle)

        // Create the Group Elements Container
        const newSectionGroupContainer = document.createElement('div')
        newSectionGroupContainer.classList.add('group-elements-container')
        // Loop inside the Elements
        let tagIndex = 0
        for (elmnt in cardInfo.sections[section]) {
            if (((cardInfo.sections[section].tag[tagIndex] == 'video') | (cardInfo.sections[section].tag[tagIndex] == 'test')) && elmnt != 'tag' && elmnt != 'title') {
                // Create Group Element
                newSectionGroupContainer.innerHTML += `
                    <div class="group-element" onclick="openIframe('${page}', '${card}', '${section}', '${elmnt}')">
                        <div class="group-element-tag">
                            <img src="./assets/${cardInfo.sections[section].tag[tagIndex]}.png">
                        </div>
                        <div class="group-element-text">${elmnt}</div>
                    </div>
                `
                tagIndex++
            } else if ((cardInfo.sections[section].tag[tagIndex] == 'file') && elmnt != 'tag' && elmnt != 'title') {
                // Create Group Element
                newSectionGroupContainer.innerHTML += `
                    <a href="${cardInfo.sections[section][elmnt]}" target="_blank">
                        <div class="group-element">
                            <div class="group-element-tag">
                                <img src="./assets/${cardInfo.sections[section].tag[tagIndex]}.png">
                            </div>
                            <div class="group-element-text">${elmnt}</div>
                        </div>
                    </a>

                `
                tagIndex++
            }
        }
        newSection.appendChild(newSectionGroupContainer)
        cardInnerContainer.appendChild(newSection)

        const newHrElmnt = document.createElement('hr')
        cardInnerContainer.appendChild(newHrElmnt)
    }
    cardContainer.appendChild(cardInnerContainer)

    return cardContainer
}

function createPage(cardList, parent) {
    let scrollContainer = document.createElement('div')
    scrollContainer.classList.add('flex-container')
    for (const card in cardList) {
        scrollContainer.appendChild(createNewCardContainer(parent, cardList[card], card))
    }
    document.querySelector(`main.${parent}`).appendChild(scrollContainer)
}
createPage(cardsContainer1, "page-1")
createPage(cardsContainer2, "page-3")




function switchPage(pageNum) {
    document.querySelectorAll('main').forEach(elmnt => {
        if (!elmnt.classList.contains('hidden')) {
            elmnt.classList.add('hidden')
        }
    })
    document.querySelector(`.page-${pageNum}`).classList.remove('hidden')

    document.querySelectorAll('.page-switch').forEach(elmnt => {
        if (elmnt.classList.contains('page-switch-active')) {
            elmnt.classList.remove('page-switch-active')
        }
    })
    document.querySelector(`.page-switch-${pageNum}`).classList.add('page-switch-active')
}
function killIframe() {
    let iframe = document.querySelector('.iframe-container')
    let iframeKiller = document.querySelector('.iframe-container-killer')
    if (iframe) {
        iframe.remove()
        iframeKiller.remove()
    }
}
function openIframe(parent, card, section, name) {
    killIframe()
    var pageCardsContainer
    if(parent == "page-1"){
        pageCardsContainer = cardsContainer1
    }else if(parent == "page-3"){
        pageCardsContainer = cardsContainer2
    }
    document.body.innerHTML += `

        <div class="iframe-container">
            <iframe class="iframe" src="${pageCardsContainer[card].sections[section][name]}" width="560" height="315" frameborder="0" allowfullscreen></iframe>
            <div class="iframe-killer" onclick="killIframe()"></div>
        </div>
    `
}
